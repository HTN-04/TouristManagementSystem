    public NguoiDung getDangKy() {
        String hoVaTen = tfHoVaTen.getText().trim();
        String tenDangNhap = tfTenDangNhap.getText().trim();
        String matKhau = new String(pfMatKhau.getPassword());
        String nhapLaiMatKhau = new String(pfNhapLaiMatKhau.getPassword());
        String email = tfEmail.getText().trim();
        String sdt = tfSoDienThoai.getText().trim();
        String vaiTro;
        if (!validateDangKy(hoVaTen, tenDangNhap, sdt, email)) {
            return null;
        }
        if (!validateMatKhau(matKhau, nhapLaiMatKhau)) {
            return null;
        }
        if (rbtnChuDichVu.isSelected()) {
            vaiTro = "chudichvu";
            return new ChuDichVu(hoVaTen, tenDangNhap, matKhau, email, sdt, vaiTro);
        } else {
            vaiTro = "khachdulich";
            return new KhachDuLich(hoVaTen, tenDangNhap, matKhau, email, sdt, vaiTro);
        }
    }

