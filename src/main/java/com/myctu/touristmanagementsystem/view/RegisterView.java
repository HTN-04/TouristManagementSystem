    public boolean validateDangKy(String hoVaTen, String tenDangNhap, String sdt, String email) {
        if (hoVaTen == null || tenDangNhap == null || sdt == null || email == null) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống!");
            return false;
        }
        if (hoVaTen.isBlank() || tenDangNhap.isBlank() || sdt.isBlank() || email.isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống!");
            return false;
        }
        return true;
    }

    public boolean validateMatKhau(String pwd, String confirmPwd) {
        if (pwd == null || pwd.isBlank() || confirmPwd == null || confirmPwd.isBlank()) {
            JOptionPane.showMessageDialog(rootPane, "Không được để trống mật khẩu");
            return false;
        }
        if (!(confirmPwd.equals(pwd))) {
            JOptionPane.showMessageDialog(rootPane, "Mật khẩu không khớp !");
            return false;
        }
        return true;
    }

